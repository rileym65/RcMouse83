~ Program to demonstrate setting and retrieving a
~ value using a pointer

0 C:        ~ Clear C
C A:        ~ Set A as a pointer to C
5 A.:       ~ Indirectly store 5 into C
C. ! "!"    ~ Print C to see that 5 is there
7 C:        ~ Now set C to 7
A.. ! "!"   ~ Indirectly print the contents of C
$

